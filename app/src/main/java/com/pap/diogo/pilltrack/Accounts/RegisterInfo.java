package com.pap.diogo.pilltrack.Accounts;

public class RegisterInfo {
    public String name;
    public String age;
    public String sex;
    public String weight;
    public String height;

    public RegisterInfo(String name, String age, String sex, String weight, String height) {
        this.name = name;
        this.age = age;
        this.sex = sex;
        this.weight = weight;
        this.height = height;
    }
}
